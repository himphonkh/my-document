    private ImageIcon ResizeImage(String path) {
       ImageIcon myimage=new ImageIcon(path);
       Image img=myimage.getImage();
       Image newimg=img.getScaledInstance(txtIconImage.getWidth(),txtIconImage.getHeight(), Image.SCALE_SMOOTH);
       ImageIcon image=new ImageIcon(newimg);
       return image;
    }
    
    // count number from database
    private int CountNumber() {
        int count = 0;
        myDB db = new myDB();
        Connection conn = db.getConnection();
        String q = "select * from word";
        try {
            Statement stmt = null;
            stmt = conn.createStatement();
            try (ResultSet rs = stmt.executeQuery(q)) {
                while (rs.next()){
                    count++;
                }
            }
        } catch (Exception e) {
        }
        return ++count;
    }
