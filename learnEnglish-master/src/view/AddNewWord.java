    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // Click to save file to sql
        
        String vacabulary = txtWord.getText();
        String position = txtPosition.getText();
        String symbol = txtSpell.getText();
        String hash = txtHash.getText();
        String definition = txtDefinition.getText();
        String example = txtExample.getText();
        String image = s;
//        String audio = txtAudio.getText();
        
        try {
            myDB db = new myDB();
            Connection con = db.getConnection();
            String sql = "insert into word values ( ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            File file = new File(s);
            File fileAudio = new File(pathAudio);
            FileInputStream is = new FileInputStream(file);
            FileInputStream isAudio = new FileInputStream(fileAudio);
            int count = CountNumber();
            
            try (java.sql.PreparedStatement st = con.prepareStatement(sql)){
                st.setInt(1, count);
                st.setString(2, vacabulary);
                st.setString(3, position);
                st.setString(4, symbol);
                st.setString(5, hash);
                st.setString(6, definition);
                st.setString(7, example);
                st.setBinaryStream(8, is, (int) file.length());
                st.setBinaryStream(9, isAudio, (int) fileAudio.length());

                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Database stored successfully");
                st.close();
            } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e);
            }
                        
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        ViewWord vw = new ViewWord();
        vw.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSaveMouseClicked

