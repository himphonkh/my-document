    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        // edit form
        int row = jTable.getSelectedRow();
        ArrayList<Word> list = wordList();
        
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        int selected_id = (int) model.getValueAt(row, 0);
        
        if ( row == -1 ) {
            JOptionPane.showMessageDialog(null, "Hãy chọn dòng có sinh viên cần sửa rồi ấn Sửa!");
        } else if ( list.isEmpty()){
            JOptionPane.showMessageDialog(null, "Bảng cuẩ bạn không có dữ liệu!");
        } else {
            EditWord edit = new EditWord();
            edit.setEdit(list.get(row));
            edit.setVisible(true);
        }
    }//GEN-LAST:event_btnEditMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // Delete row from mySql
        int row = jTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        
        int selected_id = (int) model.getValueAt(row, 0);
        if(row >= 0 ){
            model.removeRow(row);
            try {
                myDB db = new myDB();
                Connection con = db.getConnection();
                
                PreparedStatement st = con.prepareStatement("delete from word where id = " + selected_id);
                st.executeUpdate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

