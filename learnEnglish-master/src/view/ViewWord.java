    private ArrayList<Word> wordList (){
        wordList = new ArrayList<>();
        try {
            myDB db = new myDB();
            Connection con = db.getConnection();
            String sql = "select * from word";
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            Word wordDB;
            while(rs.next()){
                wordDB = new Word(rs.getInt("id"), rs.getString("word"), rs.getString("position"), rs.getString("symbol"), rs.getString("hash"), rs.getString("definition"), rs.getString("example"));
                wordList.add(wordDB);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        return wordList;        
    }
    
    public void show_word(){
        ArrayList<Word> list = wordList();
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        Object[] row = new Object[7];
        
        for(int i = 0 ; i < list.size() ; i++){
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getWord();
            row[2] = list.get(i).getPosition();
            row[3] = list.get(i).getSymbol();
            row[4] = list.get(i).getHash();
            row[5] = list.get(i).getDefinition();
            row[6] = list.get(i).getExample();
            model.addRow(row);
        }
        
    }

